"""
Tipo None
O tipo de dado None em Python representa o tipo sem tipo, conhecido também como tipo vazio

OBS: O tipo None é sempre especificado com a primeira letra maiúscula

Quando utilizar?
Pode ser utilizado quando queremos criar uma variavel e inicializa-la com um tipo
sem tipo, antes de recebemos um valor final
"""

numeros = None
print(numeros)
print(type(numeros))

numeros = 1.44, 1.34, 5.67

print(numeros)
print(type(numeros))